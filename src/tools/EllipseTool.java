package tools;

import java.awt.geom.Ellipse2D;

/**
 * This is a class for ellipse tool in this program.
 * 
 * @author Qing Bai
 * @version 21 February 2015
 */
public final class EllipseTool extends AbstractRectEllipseTool {

    /**
     * This is a constructor of this class.
     */
    public EllipseTool() {
        super(new Ellipse2D.Double());
    }
    
    /**
     * {@inheritDoc}
     */
    @Override
    public String getName() {
        return "Ellipse";
    }
}